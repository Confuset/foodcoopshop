namespace App\Lib\RemoteFile;

class RemoteFile
{
    public static function exists(string $remoteFile): bool
    {

        $ch = curl_init($remoteFile);
        curl_setopt($ch, CURLOPT_NOBODY, true);
        curl_exec($ch);
        $responseCode = curl_getinfo($ch, CURLINFO_HTTP_CODE);
        curl_close($ch);

        if ($responseCode == 200){
            return true;
        }

        return false;

    }

}
